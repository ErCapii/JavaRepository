package Ejercicio4ServicioPedidosAmpliacionLogin.Service;

public class ConsultException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ConsultException() {
	}

	public ConsultException(String message) {
		super(message);

	}

	public ConsultException(Throwable cause) {
		super(cause);

	}

	public ConsultException(String message, Throwable cause) {
		super(message, cause);

	}

	public ConsultException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
